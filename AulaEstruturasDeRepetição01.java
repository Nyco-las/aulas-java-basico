package introducao;

public class AulaEstruturasDeRepetição01 {
    public static void main(String[] args) {
        // while, do while, for
        // imprimir todos os numeros pares de o até 10000

        for(int i=0;i <= 10000; i++){
           //expressão utilizada para números ímpares;
           // para numeros pares basta utilizar ==;
            if(i % 2 != 0){
                System.out.println(i);
            }

        }



    }


}
